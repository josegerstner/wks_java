package scanner;
import java.util.Scanner;
public class ScaneoDeDatos {
	public static void main(String args[]){
		Scanner myVariable = new Scanner(System.in);
		System.out.println(myVariable.nextLine());
		/*Se pueden ingresar los tipos según función:
		 * nextLine()=complete line
		 * next()=word
		 * nextByte()=byte
		 * nextShort=short
		 * nextInt()=int
		 * nextLong()=long
		 * nextFloat()=float
		 * nextDouble()=double
		 * nextBoolean()=boolean
		*/
	}
}
